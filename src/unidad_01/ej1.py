"""
Cree un programa que tome tres valores por consola multiplique
el primero por el segundo y le sume el tercero.
Presente el resultado en la terminal
"""

v1 = int(input("ingrese primer valor:"))
v2 = int(input("ingrese segundo valor:"))
v3 = int(input("ingrese tercer valor:"))

res = (v1 * v2) + v3

print(f"el resultado es: {res}")
